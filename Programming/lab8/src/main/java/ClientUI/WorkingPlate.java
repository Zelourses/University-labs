    public void initiate(ConcurrentHashMap<Integer,Inflammable> coll){
        this.collection = coll;
        jTable1.getTableHeader().setReorderingAllowed(false);
        jButton1.setEnabled(false);
        jButton5.setEnabled(false);
        ((DefaultTableModel)jTable1.getModel()).setNumRows(0);
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        coll.forEach((k,v)->{
            model.addRow(new Object[]{k,v.getName(),v.getBurningPower(),v.getSize(),v.getOwner(),v.getOffsettime(),v.getThing()});
        });
        jPanel3.setCol(coll);
    }

    public void addInflams(ConcurrentHashMap<Integer,Inflammable> collection){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        collection.forEach((k,v)->{
                    collection.put(k, v);
                    model.addRow(new Object[]{k,v.getName(),v.getBurningPower(),v.getSize(),v.getOwner(),v.getOffsettime(),v.getThing()});
                }
        );
        jPanel3.addInflams(collection);
    }

    public void removeInflam(Integer id){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        for(int count= 1; count<= model.getRowCount(); count++){
            Integer currentID = (Integer)model.getValueAt(count, 0);
            if(id.equals(currentID)){
                model.removeRow(count);
                break;
            }
        }
        collection.remove(id);
        jPanel3.removeInflam(id);
    }
    public void removeInflams(ArrayList<Integer> list){
        list.forEach(e -> {
            removeInflam(e);
            collection.remove(e);
        });
        jPanel3.removeInflams(list);
    }

    public void ChangeInflam(Inflammable inflammable){
        collection.remove(inflammable.getId());
        collection.put(inflammable.getId(),inflammable);
        jPanel3.changeInflam(inflammable.getId(),inflammable);
    }

