    private Inflammable SelectedInflam(){
        String name = jTextField2.getText();
        float butrningpower = jSlider1.getValue();
        float size = jSlider2.getValue();
        int sizeui=jSlider3.getValue();
        String ThingName=jTextField1.getText();
        int Id=currentObject;
        String owner = worker.login;
        if (name.equals("")||butrningpower==0||size==0||sizeui==0||ThingName.equals("")){
            return null;
        }
        Inflammable inflam = new Inflammable(name,butrningpower,size,new Thing(ThingName));
        inflam.setSizeUI(sizeui);
        inflam.setOwner(owner);
        inflam.setOffsettime(OffsetDateTime.now());
        inflam.setId(Id);
        return inflam;

    }
